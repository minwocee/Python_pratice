    # ==연산 : 두 리스트가 같은지 비교(요소의 값과 순서 모든게 일치해야 참)
    # !=연산 : 두 리스트가 다른지 비교
    # <, > : 두 리스트의 대-소를 비겨하는 연산을 두 리스트의 요소를 사전적 순서로 비교하여 결과를 반환





