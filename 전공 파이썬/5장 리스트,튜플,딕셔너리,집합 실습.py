                                # <리스트의 인덱싱과 슬라이싱 기능>
# 인덱싱 기초
# 리스트이름[0] : 리스트의 첫번쨰 요소 반환
# 리스트이름[-1] : 리스트의 마지막 요소 반환
# 리스트이름[n:] : 리스트의 [n]~[-1]까지 슬라이싱
# 리스트이름[:n] : 리스트의 [0]~[n-1]까지 슬라이싱(n은 범위에 포함되지 않는다.)
# 슬라이싱 기본 형식: [시작인덱스:종료인덱스:step(증감값)] 종료인덱스-1까지 실제로 자른다. step은 생략 가능(기본으로 인덱스 1씩 처리)

